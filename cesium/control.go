type ControlUpdate struct {
	Transfers []control.Transfer `json:"transfers"`
}

// ConfigureControlUpdateChannel configures a channel to be the update channel for the
// database. If the channel is not found, it is created.
func (db *DB) ConfigureControlUpdateChannel(ctx context.Context, key ChannelKey, name string) error {
	if db.closed.Load() {
		return errDBClosed
	}
	db.mu.Lock()
	defer db.mu.Unlock()
	if db.mu.digests.inlet != nil {
		db.L.DPanic("control update channel already configured")
		return errors.New("control update channel already configured")
	}
	ch, err := db.retrieveChannel(ctx, key)
	if errors.Is(err, core.ErrChannelNotFound) {
		ch.Key = key
		ch.DataType = telem.StringT
		ch.Virtual = true
		ch.Name = name
		if err = db.createChannel(ctx, ch); err != nil {
			return err
		}
	} else if err != nil {
		return err
	}

	if ch.DataType != telem.StringT || !ch.Virtual {
		return errors.New("control update channel must be a string virtual channel.")
	}

	w, err := db.newStreamWriter(ctx, WriterConfig{
		ControlSubject: xcontrol.Subject{
			Name: "cesium_internal_control_digest",
			Key:  uuid.New().String(),
		},
		Start:    telem.Now(),
		Channels: []ChannelKey{key},
	})
	// It's important to set the digests key AFTER we create the writer, so that we
	// don't send the control transfer generated by opening this specific writer to
	// itself before it is ready to process it.
	db.mu.digests.key = key
	if err != nil {
		return err
	}
	db.mu.digests.inlet, db.mu.digests.outlet = confluence.Attach(w, 100)
	// We use a channel based mechanism to shut down this writer, so we create an
	// isolated context here.
	sCtx, cancel := signal.Isolated()
	db.mu.digests.shutdown = signal.NewHardShutdown(sCtx, cancel)
	w.Flow(
		sCtx,
		confluence.CloseOutputInletsOnExit(),
		confluence.CancelOnFail(),
		confluence.RecoverWithErrOnPanic(),
		confluence.WithAddress("control_writer"),
	)
	return nil
}

func (db *DB) updateControlDigests(
	ctx context.Context,
	u ControlUpdate,
) error {
	if !db.digestsConfigured() {
		return nil
	}
	return signal.SendUnderContext(
		ctx,
		db.mu.digests.inlet.Inlet(),
		WriterRequest{Command: WriterWrite, Frame: db.ControlUpdateToFrame(ctx, u)},
	)
}

func (db *DB) closeControlDigests() error {
	// We need to do a careful mutex lock covers both the digests key check and
	// conditional re-set to 0. After that, we need to unlock the mutex so that the
	// control digest writer can be properly shut down, as it internally needs to
	// acquire a read lock on the mutex.
	db.mu.Lock()
	if db.mu.digests.key == 0 {
		db.mu.Unlock()
		return nil
	}
	db.mu.digests.key = 0
	db.mu.Unlock()
	db.mu.digests.inlet.Close()
	confluence.Drain(db.mu.digests.outlet)
	return db.mu.digests.shutdown.Close()
}

func (db *DB) digestsConfigured() bool { return db.mu.digests.key != 0 }

// ControlStates returns the leading control resource in each unary and virtual channel
// in the Cesium database at the snapshot at which ControlStates is called: the
// controlState may change during the call.
func (db *DB) ControlStates() (u ControlUpdate) {
	db.mu.RLock()
	defer db.mu.RUnlock()
	if !db.digestsConfigured() {
		return
	}
	u.Transfers = make([]control.Transfer, 0, len(db.mu.unaryDBs)+len(db.mu.virtualDBs))
	for _, d := range db.mu.unaryDBs {
		if s := d.LeadingControlState(); s != nil {
			u.Transfers = append(u.Transfers, control.Transfer{To: s})
		}
	}
	for _, d := range db.mu.virtualDBs {
		if s := d.LeadingControlState(); s != nil {
			u.Transfers = append(u.Transfers, control.Transfer{To: s})
		}
	}
	return u
}

func (db *DB) ControlUpdateToFrame(ctx context.Context, u ControlUpdate) Frame {
	d, err := EncodeControlUpdate(ctx, u)
	if err != nil {
		panic(err)
	}
	return telem.UnaryFrame(db.mu.digests.key, d)
}

func EncodeControlUpdate(ctx context.Context, u ControlUpdate) (s telem.Series, err error) {
	s.DataType = telem.StringT
	s.Data, err = (&binary.JSONCodec{}).Encode(ctx, u)
	s.Data = append(s.Data, '\n')
	return s, err
}
