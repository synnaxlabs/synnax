package os

import "os"

// FileExists returns whether a file exists at the given path. It returns
// (true, nil) if the file exists, (false, nil) if it does not exist, and
// (false, err) for any other error (e.g. permission denied).
func FileExists(path string) (bool, error) {
	_, err := os.Stat(path)
	if err == nil {
		return true, nil
	}
	if os.IsNotExist(err) {
		return false, nil
	}
	return false, err
}
