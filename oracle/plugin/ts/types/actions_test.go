package types_test

import (
	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	"github.com/synnaxlabs/oracle/plugin/ts/types"
	. "github.com/synnaxlabs/oracle/testutil"
)

var _ = Describe("TS Actions Generation", func() {
	var (
		loader *MockFileLoader
		p      *types.Plugin
	)

	BeforeEach(func() {
		loader = NewMockFileLoader()
		p = types.New(types.DefaultOptions())
	})

	Context("structs without actions", func() {
		It("Should not emit an actions.gen.ts file", func(ctx SpecContext) {
			source := `
				@ts output "client/ts/src/user"

				User struct {
					key  uuid
					name string
				}
			`
			resp := MustGenerate(ctx, source, "user", loader, p)
			for _, f := range resp.Files {
				Expect(f.Path).ToNot(HaveSuffix("actions.gen.ts"))
			}
		})
	})

	Context("structs with actions", func() {
		It("Should emit a discriminated-union codec with reduce/reduceAll", func(ctx SpecContext) {
			source := `
				@ts output "client/ts/src/counter"

				Counter struct {
					key   uuid
					value int32

					action SetValue {
						value int32
					}

					action Increment {
					}
				}
			`
			resp := MustGenerate(ctx, source, "counter", loader, p)
			ExpectContent(resp, "actions.gen.ts").
				ToContain(
					"// Code generated by Oracle. DO NOT EDIT.",
					"export const setValuePayloadZ = z.object({",
					"export type SetValuePayload",
					"export const incrementPayloadZ = z.object({",
					"export type IncrementPayload",
					"set_value: \"set_value\"",
					"increment: \"increment\"",
					"export const actionZ = z.discriminatedUnion(\"type\", [",
					"z.literal(\"set_value\")",
					"z.literal(\"increment\")",
					"export type Action",
					"export const setValue = (payload: SetValuePayload): Action",
					"export const increment = (payload: IncrementPayload): Action",
					"export const reduce = (state: Counter, action: Action): Counter",
					"case \"set_value\":",
					"handleSetValue(state, action.setValue);",
					"case \"increment\":",
					"handleIncrement(state, action.increment);",
					"export const reduceAll = (state: Counter, actions: Action[]): Counter",
					"produce(state, (draft) => actions.forEach((action) => reduce(draft, action)))",
				)
		})

		It("Should snake_case action types and camelCase constructor names", func(ctx SpecContext) {
			source := `
				@ts output "client/ts/src/board"

				Board struct {
					key uuid

					action SetNodePosition {
						key string
					}
				}
			`
			resp := MustGenerate(ctx, source, "board", loader, p)
			ExpectContent(resp, "actions.gen.ts").
				ToContain(
					"set_node_position: \"set_node_position\"",
					"export const setNodePosition",
					"handleSetNodePosition",
					"z.literal(\"set_node_position\")",
				)
		})

		It("Should import handler functions from the sibling actions module", func(ctx SpecContext) {
			source := `
				@ts output "client/ts/src/widget"

				Widget struct {
					key uuid

					action Toggle {
					}
				}
			`
			resp := MustGenerate(ctx, source, "widget", loader, p)
			ExpectContent(resp, "actions.gen.ts").
				ToContain(
					"handleToggle",
					`from "@/widget/actions"`,
				)
		})

		It("Should import the target type from the sibling types.gen module", func(ctx SpecContext) {
			source := `
				@ts output "client/ts/src/widget"

				Widget struct {
					key uuid

					action Toggle {
					}
				}
			`
			resp := MustGenerate(ctx, source, "widget", loader, p)
			ExpectContent(resp, "actions.gen.ts").
				ToContain(
					"Widget",
					`from "@/widget/types.gen"`,
				)
		})
	})
})
