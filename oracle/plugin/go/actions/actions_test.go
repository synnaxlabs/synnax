package actions_test

import (
	"testing"

	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	"github.com/synnaxlabs/oracle/plugin/go/actions"
	. "github.com/synnaxlabs/oracle/testutil"
)

func TestGoActions(t *testing.T) {
	RegisterFailHandler(Fail)
	RunSpecs(t, "Go Actions Plugin Suite")
}

var _ = Describe("Go Actions Plugin", func() {
	var (
		loader *MockFileLoader
		p      *actions.Plugin
	)

	BeforeEach(func() {
		loader = NewMockFileLoader()
		p = actions.New(actions.DefaultOptions())
	})

	Describe("Plugin Interface", func() {
		It("Should have correct name", func() {
			Expect(p.Name()).To(Equal("go/actions"))
		})

		It("Should filter on go domain", func() {
			Expect(p.Domains()).To(Equal([]string{"go"}))
		})

		It("Should require go/types", func() {
			Expect(p.Requires()).To(Equal([]string{"go/types"}))
		})
	})

	Describe("Generate", func() {
		Context("structs without actions", func() {
			It("Should emit no files", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/user"

					User struct {
						key  uuid
						name string
					}
				`
				resp := MustGenerate(ctx, source, "user", loader, p)
				Expect(resp.Files).To(BeEmpty())
			})
		})

		Context("structs with actions", func() {
			It("Should emit a discriminated-union codec", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/counter"

					Counter struct {
						key   uuid
						value int32

						action SetValue {
							value int32
						}

						action Increment {
						}
					}
				`
				resp := MustGenerate(ctx, source, "counter", loader, p)
				Expect(resp.Files).To(HaveLen(1))

				ExpectContent(resp, "actions.gen.go").
					ToContain(
						"// Code generated by oracle. DO NOT EDIT.",
						"package counter",
						`ActionTypeSetValue = "set_value"`,
						`ActionTypeIncrement = "increment"`,
						"type SetValue struct {",
						"Value int32 `json:\"value\" msgpack:\"value\"`",
						"type Increment struct {",
						"type Action struct {",
						`Type string `+"`"+`json:"type" msgpack:"type"`+"`",
						"SetValue *SetValue",
						"Increment *Increment",
						"func (a Action) Reduce(state Counter) (Counter, error) {",
						"case ActionTypeSetValue:",
						"return a.SetValue.Handle(state)",
						"case ActionTypeIncrement:",
						"return a.Increment.Handle(state)",
						"func ReduceAll(state Counter, actions []Action) (Counter, error) {",
						"func NewSetValueAction(p SetValue) Action {",
						"func NewIncrementAction(p Increment) Action {",
					)
			})

			It("Should snake_case action names with multiple words", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/board"

					Board struct {
						key uuid

						action SetNodePosition {
							key string
						}
					}
				`
				resp := MustGenerate(ctx, source, "board", loader, p)
				ExpectContent(resp, "actions.gen.go").
					ToContain(
						`ActionTypeSetNodePosition = "set_node_position"`,
						"NewSetNodePositionAction",
					)
			})

			It("Should not pointer-prefix slice, map, or record fields", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/bag"

					Bag struct {
						key uuid

						action AddProps {
							names string[]
							props record?
						}
					}
				`
				resp := MustGenerate(ctx, source, "bag", loader, p)
				content := string(resp.Files[0].Content)
				Expect(content).ToNot(ContainSubstring("*[]"))
				Expect(content).ToNot(ContainSubstring("*msgpack.EncodedJSON"))
				Expect(content).To(ContainSubstring("Names []string"))
			})
		})
	})
})
