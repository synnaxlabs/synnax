type Options struct {
	FileNamePattern string
}

func DefaultOptions() Options {
	return Options{FileNamePattern: "actions.gen.go"}
}

type Plugin struct{ Options Options }

func New(opts Options) *Plugin { return &Plugin{Options: opts} }

func (p *Plugin) Name() string                  { return "go/actions" }
func (p *Plugin) Domains() []string             { return []string{"go"} }
func (p *Plugin) Requires() []string            { return []string{"go/types"} }
func (p *Plugin) Check(_ *plugin.Request) error { return nil }

var goPostWriter = &exec.PostWriter{
	Extensions: []string{".go"},
	Commands:   [][]string{{"gofmt", "-w"}},
}

func (p *Plugin) PostWrite(files []string) error {
	return goPostWriter.PostWrite(files)
}

func (p *Plugin) Generate(req *plugin.Request) (*plugin.Response, error) {
	resp := &plugin.Response{}
	for _, typ := range req.Resolutions.StructTypes() {
		form, ok := typ.Form.(resolution.StructForm)
		if !ok || len(form.Actions) == 0 {
			continue
		}
		outputPath := output.GetPath(typ, "go")
		if outputPath == "" {
			continue
		}
		content, err := p.generateFile(typ, form, outputPath, req)
		if err != nil {
			return nil, err
		}
		resp.Files = append(resp.Files, plugin.File{
			Path:    outputPath + "/" + p.Options.FileNamePattern,
			Content: content,
		})
	}
	return resp, nil
}

func (p *Plugin) generateFile(
	typ resolution.Type,
	form resolution.StructForm,
	outputPath string,
	req *plugin.Request,
) ([]byte, error) {
	pkg := naming.DerivePackageName(outputPath)
	mgr := imports.NewManager()
	ctx := &resolver.Context{
		Table:                         req.Resolutions,
		OutputPath:                    outputPath,
		Namespace:                     typ.Namespace,
		RepoRoot:                      req.RepoRoot,
		DomainName:                    "go",
		SubstituteDefaultedTypeParams: true,
	}
	r := &resolver.Resolver{
		Formatter:       gotypes.GoFormatter(),
		ImportResolver:  &gotypes.GoImportResolver{RepoRoot: req.RepoRoot, CurrentPackage: pkg},
		ImportAdder:     mgr,
		PrimitiveMapper: goprimitives.Mapper(),
	}

	targetName := naming.GetGoName(typ)
	data := templateData{
		Package:    pkg,
		TargetType: targetName,
		imports:    mgr,
	}

	for _, action := range form.Actions {
		ad := actionData{
			Name:     naming.ToPascalCase(action.Name),
			TypeName: lo.SnakeCase(action.Name),
			Doc:      doc.Get(action.Domains),
		}
		for _, field := range action.Fields {
			goType := r.ResolveTypeRef(field.Type, ctx)
			if field.IsHardOptional {
				goType = "*" + goType
			}
			ad.Fields = append(ad.Fields, fieldData{
				GoName:         naming.GetFieldName(field),
				GoType:         goType,
				JSONName:       lo.SnakeCase(field.Name),
				IsHardOptional: field.IsHardOptional,
			})
		}
		data.Actions = append(data.Actions, ad)
	}

	var buf bytes.Buffer
	if err := fileTemplate.Execute(&buf, &data); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

type templateData struct {
	Package    string
	TargetType string
	Actions    []actionData
	imports    *imports.Manager
}

func (d *templateData) HasImports() bool          { return d.imports.HasImports() }
func (d *templateData) ExternalImports() []string { return d.imports.ExternalImports() }
func (d *templateData) InternalImports() []imports.InternalImportData {
	return d.imports.InternalImports()
}

type actionData struct {
	Name     string
	TypeName string
	Doc      string
	Fields   []fieldData
}

type fieldData struct {
	GoName         string
	GoType         string
	JSONName       string
	IsHardOptional bool
}

func (f fieldData) TagSuffix() string {
	if f.IsHardOptional {
		return ",omitempty"
	}
	return ""
}

