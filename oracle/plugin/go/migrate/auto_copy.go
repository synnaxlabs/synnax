// auto_copy.go generates migrate_auto.gen.go files containing AutoMigrateX
// functions that convert between frozen and live type versions.
package migrate

import (
	"bytes"
	"fmt"
	"sort"
	"text/template"

	"github.com/synnaxlabs/oracle/plugin/go/internal/naming"
	"github.com/synnaxlabs/oracle/plugin/gomod"
	"github.com/synnaxlabs/oracle/plugin/output"
	"github.com/synnaxlabs/oracle/resolution"
)

// generateAutoCopy generates a migrate_auto.gen.go file for the given types.
func generateAutoCopy(
	pkg, outputPath, repoRoot string,
	types []resolution.Type,
	diff map[string]TypeDiff,
	oldTable, newTable *resolution.Table,
) ([]byte, error) {
	c := &collector{
		pkg: pkg, outputPath: outputPath, repoRoot: repoRoot,
		diff: diff, oldTable: oldTable, newTable: newTable,
		imports: make(map[string]importEntry), generated: make(map[string]bool),
	}
	data := c.collect(types)
	if len(data.Funcs) == 0 {
		return nil, nil
	}
	var buf bytes.Buffer
	if err := autoCopyTmpl.Execute(&buf, data); err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

// --- Data types ---

type fileData struct {
	Package string
	Imports []importEntry
	Funcs   []funcData
}

type importEntry struct {
	Alias string
	Path  string
}

type funcData struct {
	GoName        string
	OldTypeName   string
	NewTypeName   string
	UsesCtx       bool
	ZeroValue     string
	Kind          string // "struct", "slice", "cast"
	Preamble      []step
	Fields        []field
	SliceElemExpr string
	SliceHasErr   bool
}

type step struct {
	Kind     string // "migrate", "optionalMigrate", "optionalCast", "sliceMigrate", "sliceCast"
	VarName  string
	Accessor string
	Call     string
	Type     string
	ElemExpr string
}

type field struct {
	Name  string
	Value string
}

type classification struct {
	needsPreamble bool
	inline        string
	step          step
	usesCtx       bool
}

// --- Template ---

var autoCopyTmpl = template.Must(template.New("autoCopy").Parse(`// Code generated by oracle. DO NOT EDIT.

package {{.Package}}

import (
{{- range .Imports}}
	{{.Alias}} "{{.Path}}"
{{- end}}
)
{{range $fn := .Funcs}}
{{- if eq $fn.Kind "cast"}}
func AutoMigrate{{$fn.GoName}}({{- if $fn.UsesCtx}}ctx{{else}}_{{end}} gorp.MigrationContext, old {{$fn.OldTypeName}}) ({{$fn.NewTypeName}}, error) {
	return {{$fn.NewTypeName}}(old), nil
}
{{else if eq $fn.Kind "slice"}}
func AutoMigrate{{$fn.GoName}}({{- if $fn.UsesCtx}}ctx{{else}}_{{end}} gorp.MigrationContext, old {{$fn.OldTypeName}}) ({{$fn.NewTypeName}}, error) {
	result := make({{$fn.NewTypeName}}, len(old))
	for i, v := range old {
{{- if $fn.SliceHasErr}}
		var err error
		if result[i], err = {{$fn.SliceElemExpr}}; err != nil {
			return nil, err
		}
{{- else}}
		result[i] = {{$fn.SliceElemExpr}}
{{- end}}
	}
	return result, nil
}
{{else if eq $fn.Kind "struct"}}
func AutoMigrate{{$fn.GoName}}({{- if $fn.UsesCtx}}ctx{{else}}_{{end}} gorp.MigrationContext, old {{$fn.OldTypeName}}) ({{$fn.NewTypeName}}, error) {
{{- range $fn.Preamble}}
{{- if eq .Kind "migrate"}}
	{{.VarName}}, err := {{.Call}}
	if err != nil {
		return {{$fn.ZeroValue}}, err
	}
{{- else if eq .Kind "optionalMigrate"}}
	var {{.VarName}} *{{.Type}}
	if {{.Accessor}} != nil {
		v, err := {{.Call}}
		if err != nil {
			return {{$fn.ZeroValue}}, err
		}
		{{.VarName}} = &v
	}
{{- else if eq .Kind "optionalCast"}}
	var {{.VarName}} *{{.Type}}
	if {{.Accessor}} != nil {
		v := {{.Type}}(*{{.Accessor}})
		{{.VarName}} = &v
	}
{{- else if eq .Kind "sliceMigrate"}}
	{{.VarName}} := make({{.Type}}, len({{.Accessor}}))
	for i, v := range {{.Accessor}} {
		var err error
		if {{.VarName}}[i], err = {{.ElemExpr}}; err != nil {
			return {{$fn.ZeroValue}}, err
		}
	}
{{- else if eq .Kind "sliceCast"}}
	{{.VarName}} := make({{.Type}}, len({{.Accessor}}))
	for i, v := range {{.Accessor}} {
		{{.VarName}}[i] = {{.ElemExpr}}
	}
{{- end}}
{{- end}}
	return {{$fn.NewTypeName}}{
{{- range $fn.Fields}}
		{{.Name}}: {{.Value}},
{{- end}}
	}, nil
}
{{end}}
{{- end}}`))

// --- Collector ---

type collector struct {
	pkg, outputPath, repoRoot string
	diff                      map[string]TypeDiff
	oldTable, newTable        *resolution.Table
	imports                   map[string]importEntry
	generated                 map[string]bool
	pending                   []resolution.Type
	funcs                     []funcData
}

func (c *collector) collect(types []resolution.Type) fileData {
	for _, typ := range types {
		td, hasDiff := c.diff[typ.QualifiedName]
		if !hasDiff || td.Kind == TypeUnchanged {
			continue
		}
		c.ensureFunc(typ)
	}
	for len(c.pending) > 0 {
		typ := c.pending[0]
		c.pending = c.pending[1:]
		c.ensureFunc(typ)
	}
	// Always import gorp because every AutoMigrate function signature
	// includes gorp.MigrationContext as a parameter.
	imps := []importEntry{{Path: "github.com/synnaxlabs/x/gorp"}}
	for _, imp := range c.imports {
		imps = append(imps, imp)
	}
	sort.Slice(imps, func(i, j int) bool { return imps[i].Path < imps[j].Path })
	return fileData{Package: c.pkg, Imports: imps, Funcs: c.funcs}
}

func (c *collector) ensureFunc(typ resolution.Type) {
	if c.generated[typ.QualifiedName] {
		return
	}
	c.generated[typ.QualifiedName] = true
	switch form := typ.Form.(type) {
	case resolution.StructForm:
		c.funcs = append(c.funcs, c.structFunc(typ, form))
	case resolution.AliasForm:
		c.funcs = append(c.funcs, c.aliasFunc(typ, form))
	case resolution.DistinctForm:
		c.funcs = append(c.funcs, c.distinctFunc(typ, form))
	default:
		c.funcs = append(c.funcs, c.castFunc(typ))
	}
}

func (c *collector) structFunc(typ resolution.Type, sf resolution.StructForm) funcData {
	newType, _ := c.newTable.Get(typ.QualifiedName)
	newSF, _ := newType.Form.(resolution.StructForm)
	return c.structFuncFromForms(
		naming.GetGoName(typ),
		c.resolveTypeName(typ, c.oldTable),
		c.resolveNewTypeName(typ),
		sf, newSF,
	)
}

func (c *collector) aliasFunc(typ resolution.Type, form resolution.AliasForm) funcData {
	goName := naming.GetGoName(typ)
	oldName := c.resolveTypeName(typ, c.oldTable)
	newName := c.resolveNewTypeName(typ)
	if isArr, elemRef := isArrayAlias(form, c.oldTable); isArr {
		return c.sliceFunc(typ, goName, oldName, newName, elemRef)
	}
	targetResolved, ok := form.Target.Resolve(c.oldTable)
	if !ok {
		targetResolved, ok = c.oldTable.Lookup(typ.Namespace, form.Target.Name)
	}
	if ok {
		if sf, isStruct := targetResolved.Form.(resolution.StructForm); isStruct {
			newTarget, _ := c.newTable.Get(targetResolved.QualifiedName)
			newSF, _ := newTarget.Form.(resolution.StructForm)
			oldSF := substituteTypeParams(sf, form.Target.TypeArgs)
			if newAlias, ok := c.newTable.Get(typ.QualifiedName); ok {
				if af, ok := newAlias.Form.(resolution.AliasForm); ok {
					newSF = substituteTypeParams(newSF, af.Target.TypeArgs)
				}
			}
			return c.structFuncFromForms(goName, oldName, newName, oldSF, newSF)
		}
	}
	return c.castFunc(typ)
}

func (c *collector) distinctFunc(typ resolution.Type, form resolution.DistinctForm) funcData {
	goName := naming.GetGoName(typ)
	oldName := c.resolveTypeName(typ, c.oldTable)
	newName := c.resolveNewTypeName(typ)
	if form.Base.Name == "Array" && len(form.Base.TypeArgs) > 0 {
		return c.sliceFunc(typ, goName, oldName, newName, form.Base.TypeArgs[0])
	}
	return c.castFunc(typ)
}

func (c *collector) castFunc(typ resolution.Type) funcData {
	return funcData{
		GoName:      naming.GetGoName(typ),
		OldTypeName: c.resolveTypeName(typ, c.oldTable),
		NewTypeName: c.resolveNewTypeName(typ),
		Kind:        "cast",
	}
}

func (c *collector) structFuncFromForms(
	goName, oldTypeName, newTypeName string,
	oldSF, newSF resolution.StructForm,
) funcData {
	fn := funcData{
		GoName: goName, OldTypeName: oldTypeName, NewTypeName: newTypeName,
		ZeroValue: newTypeName + "{}", Kind: "struct",
	}
	for i, ext := range oldSF.Extends {
		if i >= len(newSF.Extends) {
			continue
		}
		oldParent, ok := c.resolveRef(ext)
		if !ok {
			continue
		}
		name := naming.GetGoName(oldParent)
		c.addField(&fn, ext, "old."+name, name, false)
	}
	for _, oldField := range oldSF.Fields {
		if findField(newSF.Fields, oldField.Name) == nil {
			continue
		}
		name := naming.GetFieldName(oldField)
		c.addField(&fn, oldField.Type, "old."+name, name, oldField.IsHardOptional)
	}
	return fn
}

func (c *collector) addField(fn *funcData, ref resolution.TypeRef, accessor, goName string, isOptional bool) {
	cls := c.classifyField(ref, accessor, goName, isOptional)
	if cls.needsPreamble {
		fn.Preamble = append(fn.Preamble, cls.step)
		fn.Fields = append(fn.Fields, field{Name: goName, Value: cls.step.VarName})
		fn.UsesCtx = fn.UsesCtx || cls.usesCtx
	} else {
		fn.Fields = append(fn.Fields, field{Name: goName, Value: cls.inline})
	}
}

func (c *collector) sliceFunc(
	typ resolution.Type,
	goName, oldTypeName, newTypeName string,
	elemRef resolution.TypeRef,
) funcData {
	elemResolved, ok := c.resolveRef(elemRef)
	if !ok {
		elemResolved, ok = c.oldTable.Lookup(typ.Namespace, elemRef.Name)
	}
	if !ok {
		return funcData{GoName: goName, OldTypeName: oldTypeName, NewTypeName: newTypeName, Kind: "cast"}
	}
	td, hasDiff := c.diff[elemResolved.QualifiedName]
	needsMigration := hasDiff && td.Kind != TypeUnchanged
	if needsMigration || c.hasOracleDefinedFields(elemResolved) {
		return funcData{
			GoName: goName, OldTypeName: oldTypeName, NewTypeName: newTypeName,
			Kind: "slice", UsesCtx: true, SliceHasErr: true,
			SliceElemExpr: c.requireFunc(elemResolved) + "(ctx, v)",
		}
	}
	if c.isOracleDefined(elemResolved) {
		return funcData{
			GoName: goName, OldTypeName: oldTypeName, NewTypeName: newTypeName,
			Kind: "slice", SliceElemExpr: c.resolveNewTypeName(elemResolved) + "(v)",
		}
	}
	return funcData{GoName: goName, OldTypeName: oldTypeName, NewTypeName: newTypeName, Kind: "cast"}
}

// --- Field classification ---

func (c *collector) classifyField(
	ref resolution.TypeRef, accessor, goName string, isOptional bool,
) classification {
	resolved, ok := c.resolveRef(ref)
	if !ok {
		return classification{inline: accessor}
	}
	if bgf, ok := resolved.Form.(resolution.BuiltinGenericForm); ok {
		if bgf.Name == "Array" && len(ref.TypeArgs) > 0 {
			return c.classifySlice(ref.TypeArgs[0], accessor, goName, "[]")
		}
		return classification{inline: accessor}
	}
	if elemRef, ok := arrayBaseRef(resolved); ok {
		return c.classifySlice(elemRef, accessor, goName, c.resolveNewTypeName(resolved))
	}
	if !c.isOracleDefined(resolved) {
		return classification{inline: accessor}
	}
	td, hasDiff := c.diff[resolved.QualifiedName]
	needsMigration := hasDiff && td.Kind != TypeUnchanged
	varName := naming.LowerFirst(goName)
	if needsMigration || c.hasOracleDefinedFields(resolved) {
		helper := c.requireFunc(resolved)
		if isOptional {
			newType := c.resolveNewTypeName(resolved)
			return classification{needsPreamble: true, usesCtx: true, step: step{
				Kind: "optionalMigrate", VarName: varName, Accessor: accessor,
				Call: fmt.Sprintf("%s(ctx, *%s)", helper, accessor), Type: newType,
			}}
		}
		return classification{needsPreamble: true, usesCtx: true, step: step{
			Kind: "migrate", VarName: varName,
			Call: fmt.Sprintf("%s(ctx, %s)", helper, accessor),
		}}
	}
	newType := c.resolveNewTypeName(resolved)
	if isOptional {
		return classification{needsPreamble: true, step: step{
			Kind: "optionalCast", VarName: varName, Accessor: accessor, Type: newType,
		}}
	}
	return classification{inline: fmt.Sprintf("%s(%s)", newType, accessor)}
}

func (c *collector) classifySlice(
	elemRef resolution.TypeRef, accessor, goName, sliceTypePrefix string,
) classification {
	elemResolved, ok := c.resolveRef(elemRef)
	if !ok {
		return classification{inline: accessor}
	}
	td, hasDiff := c.diff[elemResolved.QualifiedName]
	needsMigration := hasDiff && td.Kind != TypeUnchanged
	if !needsMigration && !c.isOracleDefined(elemResolved) {
		return classification{inline: accessor}
	}
	varName := naming.LowerFirst(goName)
	newElem := c.resolveNewTypeName(elemResolved)
	sliceType := sliceTypePrefix
	if sliceTypePrefix == "[]" {
		sliceType = "[]" + newElem
	}
	if needsMigration || c.hasOracleDefinedFields(elemResolved) {
		return classification{needsPreamble: true, usesCtx: true, step: step{
			Kind: "sliceMigrate", VarName: varName, Accessor: accessor,
			Type: sliceType, ElemExpr: c.requireFunc(elemResolved) + "(ctx, v)",
		}}
	}
	return classification{needsPreamble: true, step: step{
		Kind: "sliceCast", VarName: varName, Accessor: accessor,
		Type: sliceType, ElemExpr: newElem + "(v)",
	}}
}

// --- Type resolution helpers ---

func (c *collector) requireFunc(typ resolution.Type) string {
	goName := naming.GetGoName(typ)
	goPath := output.GetPath(typ, "go")
	isLocal := goPath == c.outputPath
	if !isLocal {
		if nt, ok := c.newTable.Get(typ.QualifiedName); ok {
			isLocal = output.GetPath(nt, "go") == c.outputPath
		}
	}
	if !isLocal {
		td, ok := c.diff[typ.QualifiedName]
		isLocal = !ok || td.Kind == TypeUnchanged
	}
	if isLocal {
		if !c.generated[typ.QualifiedName] {
			c.pending = append(c.pending, typ)
		}
		return "AutoMigrate" + goName
	}
	// For TypeChanged types in external packages, call MigrateX (the developer
	// template) instead of AutoMigrateX, so developer customization takes effect.
	prefix := "AutoMigrate"
	if td, ok := c.diff[typ.QualifiedName]; ok && td.Kind == TypeChanged {
		prefix = "Migrate"
	}
	imp := c.addImport(goPath)
	return imp.Alias + "." + prefix + goName
}

func (c *collector) resolveTypeName(typ resolution.Type, table *resolution.Table) string {
	t, ok := table.Get(typ.QualifiedName)
	if !ok {
		return naming.GetGoName(typ)
	}
	goName := naming.GetGoName(t)
	goPath := output.GetPath(t, "go")
	if goPath == "" || goPath == c.outputPath {
		return goName
	}
	return c.addImport(goPath).Alias + "." + goName
}

func (c *collector) resolveNewTypeName(typ resolution.Type) string {
	nt, ok := c.newTable.Get(typ.QualifiedName)
	if !ok {
		return naming.GetGoName(typ)
	}
	return c.resolveTypeName(nt, c.newTable)
}

func (c *collector) addImport(goPath string) importEntry {
	importPath := gomod.ResolveImportPath(goPath, c.repoRoot, gomod.DefaultModulePrefix)
	if imp, ok := c.imports[importPath]; ok {
		return imp
	}
	imp := importEntry{Alias: naming.DerivePackageAlias(goPath, c.pkg), Path: importPath}
	c.imports[importPath] = imp
	return imp
}

func (c *collector) resolveRef(ref resolution.TypeRef) (resolution.Type, bool) {
	if resolved, ok := ref.Resolve(c.oldTable); ok {
		return resolved, true
	}
	if ref.Name == "" {
		return resolution.Type{}, false
	}
	for _, typ := range c.oldTable.Types {
		if typ.Name == ref.Name {
			return typ, true
		}
	}
	return resolution.Type{}, false
}

// --- Type introspection ---

func (c *collector) isOracleDefined(typ resolution.Type) bool {
	nt, ok := c.newTable.Get(typ.QualifiedName)
	return ok && output.GetPath(nt, "go") != ""
}

func (c *collector) hasOracleDefinedFields(typ resolution.Type) bool {
	sf, ok := typ.Form.(resolution.StructForm)
	if !ok {
		return c.isStructLike(typ)
	}
	for _, f := range sf.Fields {
		if c.refHasOracleType(f.Type) {
			return true
		}
	}
	for _, ext := range sf.Extends {
		if c.refHasOracleType(ext) {
			return true
		}
	}
	return false
}

func (c *collector) refHasOracleType(ref resolution.TypeRef) bool {
	for _, arg := range ref.TypeArgs {
		if c.refHasOracleType(arg) {
			return true
		}
	}
	resolved, ok := ref.Resolve(c.oldTable)
	if !ok {
		return false
	}
	return c.isOracleDefined(resolved)
}

func (c *collector) isStructLike(typ resolution.Type) bool {
	switch form := typ.Form.(type) {
	case resolution.StructForm:
		return true
	case resolution.AliasForm:
		if r, ok := form.Target.Resolve(c.oldTable); ok {
			if _, s := r.Form.(resolution.StructForm); s {
				return true
			}
		}
	case resolution.DistinctForm:
		if r, ok := form.Base.Resolve(c.oldTable); ok {
			if _, s := r.Form.(resolution.StructForm); s {
				return true
			}
		}
	}
	return false
}

// --- Pure helpers ---

func needsAutoMigrate(types []resolution.Type, diff map[string]TypeDiff) bool {
	for _, typ := range types {
		if td, ok := diff[typ.QualifiedName]; ok && td.Kind != TypeUnchanged {
			return true
		}
	}
	return false
}

func findField(fields []resolution.Field, name string) *resolution.Field {
	for i := range fields {
		if fields[i].Name == name {
			return &fields[i]
		}
	}
	return nil
}

func isArrayAlias(form resolution.AliasForm, table *resolution.Table) (bool, resolution.TypeRef) {
	if form.Target.Name == "Array" && len(form.Target.TypeArgs) > 0 {
		return true, form.Target.TypeArgs[0]
	}
	if t, ok := form.Target.Resolve(table); ok {
		if bgf, ok := t.Form.(resolution.BuiltinGenericForm); ok && bgf.Name == "Array" && len(form.Target.TypeArgs) > 0 {
			return true, form.Target.TypeArgs[0]
		}
	}
	return false, resolution.TypeRef{}
}

func arrayBaseRef(typ resolution.Type) (resolution.TypeRef, bool) {
	switch form := typ.Form.(type) {
	case resolution.AliasForm:
		if form.Target.Name == "Array" && len(form.Target.TypeArgs) > 0 {
			return form.Target.TypeArgs[0], true
		}
	case resolution.DistinctForm:
		if form.Base.Name == "Array" && len(form.Base.TypeArgs) > 0 {
			return form.Base.TypeArgs[0], true
		}
	}
	return resolution.TypeRef{}, false
}

func substituteTypeParams(sf resolution.StructForm, typeArgs []resolution.TypeRef) resolution.StructForm {
	if len(sf.TypeParams) == 0 {
		return sf
	}
	paramMap := make(map[string]resolution.TypeRef, len(sf.TypeParams))
	for i, tp := range sf.TypeParams {
		if i < len(typeArgs) {
			paramMap[tp.Name] = typeArgs[i]
		} else if tp.HasDefault() {
			paramMap[tp.Name] = *tp.Default
		}
	}
	newFields := make([]resolution.Field, len(sf.Fields))
	for i, f := range sf.Fields {
		newFields[i] = f
		newFields[i].Type = substituteRef(f.Type, paramMap)
	}
	result := sf
	result.Fields = newFields
	return result
}

func substituteRef(ref resolution.TypeRef, paramMap map[string]resolution.TypeRef) resolution.TypeRef {
	if ref.TypeParam != nil {
		if concrete, ok := paramMap[ref.TypeParam.Name]; ok {
			return concrete
		}
	}
	if len(ref.TypeArgs) > 0 {
		newArgs := make([]resolution.TypeRef, len(ref.TypeArgs))
		for i, arg := range ref.TypeArgs {
			newArgs[i] = substituteRef(arg, paramMap)
		}
		result := ref
		result.TypeArgs = newArgs
		return result
	}
	return ref
}
