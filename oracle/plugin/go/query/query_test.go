package query_test

import (
	"strings"

	. "github.com/onsi/ginkgo/v2"
	. "github.com/onsi/gomega"
	"github.com/synnaxlabs/oracle/plugin/go/query"
	. "github.com/synnaxlabs/oracle/testutil"
)

var _ = Describe("Go Query Plugin", func() {
	var (
		loader *MockFileLoader
		p      *query.Plugin
	)

	BeforeEach(func() {
		loader = NewMockFileLoader()
		p = query.New(query.DefaultOptions())
	})

	Describe("Plugin Interface", func() {
		It("Should have correct name", func() {
			Expect(p.Name()).To(Equal("go/query"))
		})

		It("Should filter on go domain", func() {
			Expect(p.Domains()).To(Equal([]string{"go"}))
		})

		It("Should require go/types", func() {
			Expect(p.Requires()).To(Equal([]string{"go/types"}))
		})
	})

	Describe("Generate", func() {
		Context("minimal struct with no filters or search", func() {
			It("Should generate a Retrieve struct with standard methods", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/foo"

					Foo struct {
						key uuid {
							@key
						}
						name string
						@ontology type "foo"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "foo", loader, p)
				Expect(resp.Files).To(HaveLen(1))

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"// Code generated by oracle. DO NOT EDIT.",
						"package foo",
						"type Retrieve struct",
						"gorp.Retrieve[uuid.UUID, Foo]",
						"func (r Retrieve) WhereKeys(keys ...uuid.UUID) Retrieve",
						"func (r Retrieve) Entry(e *Foo) Retrieve",
						"func (r Retrieve) Entries(es *[]Foo) Retrieve",
						"func (r Retrieve) Limit(limit int) Retrieve",
						"func (r Retrieve) Offset(offset int) Retrieve",
						"func (r Retrieve) Exec(ctx context.Context, tx gorp.Tx) error",
						"func (r Retrieve) Count(ctx context.Context, tx gorp.Tx) (int, error)",
						"func (r Retrieve) Exists(ctx context.Context, tx gorp.Tx) (bool, error)",
					).
					ToNotContain(
						"Search(",
						"execSearch",
						"\"github.com/samber/lo\"",
					)
			})
		})

		Context("search", func() {
			It("Should generate search methods with ontology type inferred from @ontology", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/foo"

					Foo struct {
						key uuid {
							@key
						}
						name string
						@ontology type "foo"
						@retrieve
						@search
					}
				`
				resp := MustGenerate(ctx, source, "foo", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"func (r Retrieve) Search(term string) Retrieve",
						"search     *search.Index",
						"searchTerm string",
						"ontology.ResourceTypeFoo",
						"func (r Retrieve) execSearch(ctx context.Context) (Retrieve, error)",
					)
			})

			It("Should use explicit @search type override", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/ranger"

					Ranger struct {
						key uuid {
							@key
						}
						name string
						@ontology type "ranger"
						@retrieve
						@search type "range"
					}
				`
				resp := MustGenerate(ctx, source, "ranger", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain("ontology.ResourceTypeRange").
					ToNotContain("ontology.ResourceTypeRanger")
			})
		})

		Context("filters", func() {
			It("Should generate a bool filter", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/task"

					Task struct {
						key uuid {
							@key
						}
						internal bool {
							@filter
						}
						@ontology type "task"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "task", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, uuid.UUID, Task]",
						"func MatchInternal(v bool) Filter",
					).
					ToNotContain(
						"MatchInternals(",
						"lo.Contains",
						"func (r Retrieve) MatchInternal(",
					)
			})

			It("Should generate a scalar filter", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/workspace"

					Workspace struct {
						key uuid {
							@key
						}
						author uuid {
							@filter scalar
						}
						@ontology type "workspace"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "workspace", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, uuid.UUID, Workspace]",
						"func MatchAuthor(v uuid.UUID) Filter",
					).
					ToNotContain(
						"MatchAuthors(",
						"func (r Retrieve) MatchAuthor(",
					)
			})

			It("Should generate a slice filter", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/user"

					User struct {
						key uuid {
							@key
						}
						username string {
							@filter
						}
						@ontology type "user"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "user", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, uuid.UUID, User]",
						"func MatchUsernames(vals ...string) Filter",
						"lo.Contains(vals, e.Username)",
					).
					ToNotContain("func (r Retrieve) MatchUsernames(")
			})

			It("Should generate a filter with a cross-namespace type", func(ctx SpecContext) {
				loader.Add("schemas/rack", `
					@go output "core/pkg/service/rack"

					Key = uint32
				`)

				source := `
					import "schemas/rack"

					@go output "core/pkg/service/device"

					Device struct {
						key string {
							@key
						}
						rack rack.Key {
							@filter
						}
						@ontology type "device"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "device", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, string, Device]",
						"func MatchRacks(vals ...rack.Key) Filter",
						"lo.Contains(vals, e.Rack)",
					).
					ToNotContain("func (r Retrieve) MatchRacks(")
			})
		})

		Context("custom retrieve", func() {
			It("Should not generate the Retrieve struct definition", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						name string
						@ontology type "rack"
						@retrieve custom
						@search
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"func (r Retrieve) WhereKeys(keys ...uint32) Retrieve",
						"func (r Retrieve) Entry(e *Rack) Retrieve",
						"func (r Retrieve) Entries(es *[]Rack) Retrieve",
						"func (r Retrieve) Exec(ctx context.Context, tx gorp.Tx) error",
						"func (r Retrieve) Search(term string) Retrieve",
					).
					ToNotContain("type Retrieve struct")
			})

			It("Should emit a Filter alias and Match/And/Or/Not bound combinators", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						name string
						@ontology type "rack"
						@retrieve custom
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, uint32, Rack]",
						"func Match(f func(ctx gorp.Context, r Retrieve, e *Rack) (bool, error)) Filter",
						"return gorp.MatchBound[Retrieve, uint32, Rack](f)",
						"func And(fs ...Filter) Filter",
						"return gorp.AndBound[Retrieve, uint32, Rack](fs...)",
						"func Or(fs ...Filter) Filter",
						"return gorp.OrBound[Retrieve, uint32, Rack](fs...)",
						"func Not(f Filter) Filter",
						"return gorp.NotBound[Retrieve, uint32, Rack](f)",
					)
			})

			It("Should emit filter constructors returning Filter under @retrieve custom", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						name string {
							@filter
						}
						@ontology type "rack"
						@retrieve custom
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"func MatchNames(vals ...string) Filter",
						"return func(_ Retrieve) gorp.Filter[uint32, Rack]",
					)
			})

			It("Should emit a Where method taking ...Filter and binding each filter to r", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						@ontology type "rack"
						@retrieve custom
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"func (r Retrieve) Where(filters ...Filter) Retrieve",
						"bound[i] = f(r)",
					).
					ToNotContain(
						"func (r Retrieve) Where(filters ...gorp.Filter[uint32, Rack]) Retrieve",
					)
			})

			It("Should also emit Filter machinery under a plain @retrieve (non-custom)", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						name string {
							@filter
						}
						@ontology type "rack"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Retrieve struct",
						"type Filter = gorp.BoundFilter[Retrieve, uint32, Rack]",
						"func MatchNames(vals ...string) Filter",
						"func (r Retrieve) Where(filters ...Filter) Retrieve",
						"bound[i] = f(r)",
					)
			})

			It("Should use a struct-level @retrieve key override when no field has @key", func(ctx SpecContext) {
				// Channel uses a computed GorpKey that is not stored as a field
				// on the struct. A struct-level `@retrieve key "Key"` override
				// tells oracle which declared type to use for the retrieve's
				// key type without forcing a synthetic field.
				source := `
					@go output "core/pkg/distribution/channel"

					Key uint32 {
						@go output "core/pkg/distribution/channel"
					}

					Channel struct {
						name string {
							@filter
						}
						@retrieve custom
						@retrieve key "Key"
					}
				`
				resp := MustGenerate(ctx, source, "channel", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"type Filter = gorp.BoundFilter[Retrieve, Key, Channel]",
						"func MatchNames(vals ...string) Filter",
						"return func(_ Retrieve) gorp.Filter[Key, Channel]",
						"func (r Retrieve) WhereKeys(keys ...Key) Retrieve",
					)
			})
		})

		Context("key types", func() {
			It("Should handle a string key type", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/device"

					Device struct {
						key string {
							@key
						}
						name string
						@ontology type "device"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "device", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"gorp.Retrieve[string, Device]",
						"func (r Retrieve) WhereKeys(keys ...string) Retrieve",
					)
			})

			It("Should handle a uint32 key type", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/rack"

					Rack struct {
						key uint32 {
							@key
						}
						name string
						@ontology type "rack"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "rack", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"gorp.Retrieve[uint32, Rack]",
						"func (r Retrieve) WhereKeys(keys ...uint32) Retrieve",
					)
			})
		})

		Context("ontology ID error handling in search", func() {
			It("Should generate error-checked KeysFromOntologyIDs for non-string keys", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/task"

					Task struct {
						key uuid {
							@key
						}
						name string
						@ontology type "task"
						@retrieve
						@search
					}
				`
				resp := MustGenerate(ctx, source, "task", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain("keys, err := KeysFromOntologyIDs(ids)")
			})

			It("Should generate direct KeysFromOntologyIDs for string keys", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/device"

					Device struct {
						key string {
							@key
						}
						name string
						@ontology type "device"
						@retrieve
						@search
					}
				`
				resp := MustGenerate(ctx, source, "device", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain("keys := KeysFromOntologyIDs(ids)").
					ToNotContain("keys, err := KeysFromOntologyIDs(ids)")
			})
		})

		Context("imports", func() {
			It("Should only import context and gorp for minimal structs", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/log"

					Log struct {
						key uuid {
							@key
						}
						name string
						@ontology type "log"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "log", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain(
						"\"context\"",
					).
					ToNotContain(
						"\"github.com/samber/lo\"",
					)
			})

			It("Should import lo for slice filters", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/user"

					User struct {
						key uuid {
							@key
						}
						username string {
							@filter
						}
						@ontology type "user"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "user", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain("\"github.com/samber/lo\"")
			})

			It("Should not import lo for bool-only filters", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/task"

					Task struct {
						key uuid {
							@key
						}
						internal bool {
							@filter
						}
						@ontology type "task"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "task", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToNotContain("\"github.com/samber/lo\"")
			})

			It("Should import search and ontology for @search", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/foo"

					Foo struct {
						key uuid {
							@key
						}
						name string
						@ontology type "foo"
						@retrieve
						@search
					}
				`
				resp := MustGenerate(ctx, source, "foo", loader, p)

				content := MustContentOf(resp, "retrieve.gen.go")
				Expect(content).To(ContainSubstring("search"))
				Expect(content).To(ContainSubstring("ontology"))
			})
		})

		Context("doc comments", func() {
			It("Should include doc comments on all public methods", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/foo"

					Foo struct {
						key uuid {
							@key
						}
						name string {
							@filter
						}
						active bool {
							@filter
						}
						@ontology type "foo"
						@retrieve
						@search
					}
				`
				resp := MustGenerate(ctx, source, "foo", loader, p)
				content := MustContentOf(resp, "retrieve.gen.go")

				expectedDocPrefixes := []string{
					"// Retrieve is used to retrieve Foo records",
					"// Search sets a fuzzy search term",
					"// WhereKeys filters for foos whose key",
					"// MatchNames returns a filter for foos whose Name",
					"// MatchActive returns a filter for foos by their Active",
					"// Where applies the provided filters",
					"// Entry binds the provided foo as the result",
					"// Entries binds the provided slice of foos",
					"// Limit sets the maximum number of foos",
					"// Offset sets the starting index of the foos",
					"// Exec executes the query",
					"// Count returns the number of foos",
					"// Exists checks whether any foos",
				}
				for _, prefix := range expectedDocPrefixes {
					Expect(content).To(
						ContainSubstring(prefix),
						"missing doc comment with prefix: %q", prefix,
					)
				}
			})
		})

		Context("multiple structs in same output", func() {
			It("Should generate methods for both structs in one file", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/views"

					LinePlot struct {
						key uuid {
							@key
						}
						name string
						@ontology type "lineplot"
						@retrieve
					}

					Table struct {
						key uuid {
							@key
						}
						data string
						@ontology type "table"
						@retrieve
					}
				`
				resp := MustGenerate(ctx, source, "views", loader, p)
				Expect(resp.Files).To(HaveLen(1))

				content := MustContentOf(resp, "retrieve.gen.go")
				Expect(strings.Count(content, "type Retrieve struct")).To(Equal(2))
			})
		})

		Context("struct without @retrieve is skipped", func() {
			It("Should only generate for structs with @retrieve", func(ctx SpecContext) {
				source := `
					@go output "core/pkg/service/foo"

					Foo struct {
						key uuid {
							@key
						}
						name string
						@ontology type "foo"
						@retrieve
					}

					Bar struct {
						key uuid {
							@key
						}
						data string
					}
				`
				resp := MustGenerate(ctx, source, "foo", loader, p)

				ExpectContent(resp, "retrieve.gen.go").
					ToContain("Foo").
					ToNotContain("Bar")
			})
		})
	})
})
